package com.divergentsl.corejava.datatypes;

public class DataTypes3UnsignedInteger {

	public static void main(String[] args) {

		// byte to integer 
		byte b = -126;
		System.out.println( "Unsigned Byte " + Byte.toUnsignedInt(b));
		
		long l = -2000;
		System.out.println( "Unsigned Long " + Long.toUnsignedString(l));
		
		
		
	}

}
